package com.multibrand.vo.request;

public class TOSPipelineOffersRequest {
	private String apartmentNumber; 
	private String city;
	private String country;
	private String poBox; 
	private String state;
	private String streetName;
	private String streetNumber;
	private String zip;
	private String language;
	private String promocode;
	private String companyCode;
	

	public final String getApartmentNumber() {
		return apartmentNumber;
	}

	public final void setApartmentNumber(String apartmentNumber) {
		this.apartmentNumber = apartmentNumber;
	}

	public final String getCity() {
		return city;
	}

	public final void setCity(String city) {
		this.city = city;
	}

	public final String getCountry() {
		return country;
	}

	public final void setCountry(String country) {
		this.country = country;
	}

	public final String getPoBox() {
		return poBox;
	}

	public final void setPoBox(String poBox) {
		this.poBox = poBox;
	}

	public final String getState() {
		return state;
	}

	public final void setState(String state) {
		this.state = state;
	}

	public final String getStreetName() {
		return streetName;
	}

	public final void setStreetName(String streetName) {
		this.streetName = streetName;
	}

	public final String getStreetNumber() {
		return streetNumber;
	}

	public final void setStreetNumber(String streetNumber) {
		this.streetNumber = streetNumber;
	}

	public final String getZip() {
		return zip;
	}

	public final void setZip(String zip) {
		this.zip = zip;
	}

	public final String getPromocode() {
		return promocode;
	}

	public final void setPromocode(String promocode) {
		this.promocode = promocode;
	}

	public final String getCompanyCode() {
		return companyCode;
	}

	public final void setCompanyCode(String companyCode) {
		this.companyCode = companyCode;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

}
